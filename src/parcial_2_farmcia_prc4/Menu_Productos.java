    private void btnActualizarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProductosActionPerformed
        con = establecerConexion();
        
        
        String sql ="Select * from productos";
        try{
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) dtgProductos.getModel();
            model.setRowCount(0);
            while(rs.next()){
                model.addRow(new String[] {rs.getString(1), rs.getString(2), rs.getString(3)});
            }
        } catch (Exception e){
            System.out.println("Error :" + e.getMessage());
        }
        
    }//GEN-LAST:event_btnActualizarProductosActionPerformed

    private void dtgProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dtgProductosMouseClicked
        DefaultTableModel model = (DefaultTableModel) dtgProductos.getModel();
        int selectedRowIndex = dtgProductos.getSelectedRow();
        
        txtCodProducto.setText(model.getValueAt(selectedRowIndex, 0) .toString());
        txtNomProducto.setText(model.getValueAt(selectedRowIndex, 1) .toString());
        txtPrecio.setText(model.getValueAt(selectedRowIndex, 2) .toString());        

    }//GEN-LAST:event_dtgProductosMouseClicked

    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        PreparedStatement ps = null;
        try{
            ps = realizarConsulta(con, "INSERT INTO compras (codCliente, codProducto, nomProducto, precioProducto, Cant, Total) VALUES (?, ?, ?, ?, ?, ?)" );
            ps.setInt(1, Integer.parseInt(txtCodCliente.getText()));
            ps.setInt(2, Integer.parseInt(txtCodProducto.getText()));
            ps.setString(3, txtNomProducto.getText());
            ps.setDouble(4, Double.parseDouble(txtPrecio.getText()));
            ps.setInt(5, Integer.parseInt(txtCantidad.getText()));
            ps.setDouble(6, Double.parseDouble(txtSubtotal.getText()));
            
            
            int resultado = ps.executeUpdate();
            
            if(resultado > 0){
                JOptionPane.showMessageDialog(null, "Su compra se ha realizado con exito!");
            }else{
                JOptionPane.showMessageDialog(null, "No se pudo realizar la compra 1");
            }
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "No se pudo realizar la compra 2");
        }
    }//GEN-LAST:event_btnComprarActionPerformed

